package entidad;

public class Alumno {

	private int idaAlumno, edad;
	private String nombre, apellido;
	
	public int getIdaAlumno() {
		return idaAlumno;
	}
	public void setIdaAlumno(int idaAlumno) {
		this.idaAlumno = idaAlumno;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	
	
}
